$(document).ready(function () {
    $(".Search-Category").hide();

    if ($(".input-search").val().length > 0) {
        $(".aero-search").hide();
    } else {
        $(".aero-search").show();
    }

    $(".input-search").keyup(function () {
        if ($(this).val().length > 0) {
            $(".aero-search").hide();
        } else {
            $(".aero-search").show();
        }
    });

    $(".aero-search").click(function () {
        $(".Search-Category").toggle();
    });

    $(".close-search").click(function () {
        $(".Search-Category").hide();
    });

});